        <p>很难想象，奶油蛋糕到了中国，竟会变成这个样子——植脂奶油大行其道。</p>
        <p>人类总是那么自作聪明，拒绝大自然天然的馈赠。你也许不明白，什么是植脂奶油——以植物脂肪和植物蛋白为主要成分，添加乳化剂、稳定剂、蛋白质、糖、食 盐、色素、香精等辅料，经特殊工艺加工而制成的一种奶油仿制品。在发泡性、稳定性、保形性、保存性等方面比天然奶油好，是搅打后泡沫丰富的蛋糕表面装饰、 裱花料。可话说回来，如果人工合成的东西那么完美，大自然又为何要无私馈赠呢？</p>
        <p>乳脂奶油则不同，主要从天然新鲜牛奶中提取，无任何色素和添加剂。相比之下，乳脂奶油比植脂奶油贵三倍，口感上也更加甜润地道，确切地说，是否使用乳脂奶油，是蛋糕好坏与否的根本标准。没有一个纯正的欧洲品牌，会采用植脂奶油。</p>
        <p>采用乳脂奶油的廿一客蛋糕，的确没有你曾经吃过的植脂奶油蛋糕保质期长。但就像鲜花容易凋谢，真正自然的美味，一向如此。同时这更要求我们从订单到美味送达，一定要时间极短。甚至为获得绝佳口感，连制作和享用的温度都是苛刻的，在这一点上，我们和极品干邑如出一辙。</p>
